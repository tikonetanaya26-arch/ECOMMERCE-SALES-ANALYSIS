------ BASIC QUERIES -------

SELECT * FROM CUSTOMERS;

SELECT * FROM PRODUCTS;

SELECT * FROM ORDERS;

-- PROBLEM STATEMENT : DISPLAY ELECTRONICS PRODUCTS COSTING MORE THAN RS.15000 -------
SELECT * FROM PRODUCTS 
WHERE CATEGORY = 'ELETRONICS'
AND PRICE > 15000;

---- PROBLEM STATEMENT : DISPLAY PRODUCTS THAT ARE ACCESSORIES OR PRICE MORE THAN 2000 ----
SELECT * FROM PRODUCTS 
WHERE CATEGORY = 'ACCESSORIES'
OR PRICE > 2000;

-- PROBLEM STATEMENT : DISPLAY PRODUCTS PRICED BETWEEN RS.1000 AND RS.10000 ---------
SELECT * FROM PRODUCTS 
WHERE PRICE BETWEEN 1000 AND 10000;

--- SELECT CUSTOMERS FROM PUNE AND DELHI -------------------
SELECT * FROM CUSTOMERS 
WHERE CITY IN ('PUNE','DELHI');

-- PROBLEM STATEMENT : CALCULATE TOTAL REVENUE GENERATED ------
SELECT 
SUM(P.PRICE * O.QUANTITY) AS TOTAL_REVENUE 
FROM PRODUCTS P
JOIN ORDERS O
ON P.PRODUCT_ID = O.PRODUCT_ID;

--- AVERAGE PRODUCT PRICE ------
SELECT AVG(PRICE) AS AVG_PRICE
FROM PRODUCTS;

--- MAXIMUM PRODUCT PRICE -------
SELECT MAX(PRICE) AS MAX_PRICE
FROM PRODUCTS;

-- PROBLEM STATMENT : SHOW CUSTOMER NAME, PRODUCT PURCHASED , QUANTITY AND DATE ---------
SELECT 
C.CUSTOMER_NAME,
P.PRODUCT_NAME,
O.QUANTITY,
O.ORDER_DATE
FROM CUSTOMERS C
INNER JOIN ORDERS O
ON C.CUSTOMER_ID = O.CUSTOMER_ID
INNER JOIN PRODUCTS P
ON O.PRODUCT_ID = P.PRODUCT_ID;

---- LEFT JOIN ------
SELECT 
C.CUSTOMER_NAME,
O.ORDER_ID
FROM CUSTOMERS C
LEFT JOIN ORDERS O 
ON C.CUSTOMER_ID = O.CUSTOMER_ID;

--- CATEGORY WISE REVENUE -------
SELECT
P.CATEGORY,
SUM(P.PRICE * O.QUANTITY) AS REVENUE
FROM PRODUCTS P
JOIN ORDERS O
ON P.PRODUCT_ID = O.PRODUCT_ID
GROUP BY CATEGORY;

---- REVENUE GREATER THAN RS.50000 ----
SELECT 
P.CATEGORY, 
SUM(P.PRICE * O.QUANTITY) AS REVENUE
FROM PRODUCTS P
JOIN ORDERS O
ON P.PRODUCT_ID = O.PRODUCT_ID
GROUP BY CATEGORY
HAVING SUM(P.PRICE * O.QUANTITY) > 50000;

---- PROBLEM STATEMENT : FIND CUSTOMERS WHO SPENT ABOVE AVERAGE -------------
SELECT CUSTOMER_ID 
FROM ORDERS O
JOIN PRODUCTS P
ON O.PRODUCT_ID = P.PRODUCT_ID 
GROUP BY CUSTOMER_ID 
HAVING SUM(P.PRICE * O.QUANTITY) > 
(
SELECT AVG(TOTAL_SPENT)
FROM
(
SELECT SUM(P.PRICE * O.QUANTITY) AS TOTAL_SPENT
FROM ORDERS O
JOIN PRODUCTS P
ON O.PRODUCT_ID = P.PRODUCT_ID 
GROUP BY CUSTOMER_ID 
) X 
);

----- COMMON TABLE EXPRESSION ------------
WITH CUSTOMER_SALES AS 
(
SELECT CUSTOMER_ID,
SUM(P.PRICE * O.QUANTITY) AS REVENUE 
FROM ORDERS O
JOIN PRODUCTS P
ON O.PRODUCT_ID = P.PRODUCT_ID 
GROUP BY CUSTOMER_ID 
)
SELECT * FROM CUSTOMER_SALES
WHERE REVENUE > 10000;

------ RANK CUSTOMERS ----------
SELECT 
CUSTOMER_ID,
SUM(P.PRICE * O.QUANTITY) AS TOTAL_SPENT, 
RANK() OVER 
(
ORDER BY SUM(P.PRICE * O.QUANTITY) DESC
) AS 
CUSTOMER_ID 
FROM ORDERS O
JOIN PRODUCTS P
ON O.PRODUCT_ID = P.PRODUCT_ID 
GROUP BY CUSTOMER_ID;

